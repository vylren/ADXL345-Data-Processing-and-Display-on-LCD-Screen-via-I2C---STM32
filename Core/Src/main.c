void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
    if (htim == &htim6) // Laikmacio 6 pertraukimas
    {
        // Sensoriaus nuskaitymas
        adxl_read(0x32);
        x = (data_rec[1] << 8) | data_rec[0];
        y = (data_rec[3] << 8) | data_rec[2];
        z = (data_rec[5] << 8) | data_rec[4];

        // Konvertavimas i g vienetus
        xg = x * 0.0078;
        yg = y * 0.0078;
        zg = z * 0.0078;

        // Skaiciuojame kvadratines vertes
        float x_squared = xg * xg;
        float y_squared = yg * yg;
        float z_squared = zg * zg;

        // Pridedame kvadratines vertes
        x_rms += x_squared;
        y_rms += y_squared;
        z_rms += z_squared;

        // Padidiname ciklu skaitikli
        static uint8_t cycle_count = 0;
        cycle_count++;

        // Jei praejo 30 ciklu (0,1 s * 30 = 3 s)
        if (cycle_count == 30)
        {
            // Apskaiciuojame vidutines kvadratines vertes
            x_rms = sqrt(x_rms / 30);
            y_rms = sqrt(y_rms / 30);
            z_rms = sqrt(z_rms / 30);

            // Atvaizduojame vidutines kvadratines vertes ekrane
            lcd_send_cmd(0x80);
            lcd_send_string("X=");
            lcd_send_float(x_rms, 5);

            lcd_send_cmd(0x89);
            lcd_send_string("Y=");
            lcd_send_float(y_rms, 5);

            lcd_send_cmd(0xc4);
            lcd_send_string("Z=");
            lcd_send_float(z_rms, 5);

						// Siunciame duomenis per UART
            send_sensor_data_via_uart(x_rms, y_rms, z_rms);
					
            // Išvalome sumas
            x_rms = 0.0;
            y_rms = 0.0;
            z_rms = 0.0;

            // Išvalome ciklu skaitikli
            cycle_count = 0;
					}
    }
}
