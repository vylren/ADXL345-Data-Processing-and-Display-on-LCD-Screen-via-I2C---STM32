void adxl_write (uint8_t reg, uint8_t value)
{
	uint8_t data [2];
	data[0] = reg;
	data[1] = value;
	HAL_I2C_Master_Transmit (&hi2c1, adxl_address, data, 2, 100);
}

//nuskaitymas
void adxl_read (uint8_t reg)
{
	HAL_I2C_Mem_Read (&hi2c1, adxl_address, reg, 1, (uint8_t *)data_rec, 6, 100);
}


void adxl_init (void)
{
	adxl_read(0x00); //icializavimas:
	
	adxl_write (0x2d, 0x00); //isvalo visus bitus
	adxl_write (0x2d, 0x08); //matuoja bit-1, wakeup, 0 8hz dazniu
	
	adxl_write (0x31, 0x01); //+-4g diapazone
}	
void send_sensor_data_via_uart(float x_rms, float y_rms, float z_rms)
{
    // Format sensor data into a string
    snprintf((char *)uart_tx_buffer, sizeof(uart_tx_buffer), "X_RMS: %.2f, Y_RMS: %.2f, Z_RMS: %.2f\r\n", x_rms, y_rms, z_rms);

    // Transmit the formatted string via UART
    HAL_UART_Transmit(&huart2, uart_tx_buffer, strlen((char *)uart_tx_buffer), UART_TIMEOUT);
}

